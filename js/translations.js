document.addEventListener('DOMContentLoaded', () => {
    const selector = document.getElementById('language-selector');
    
    // Si no existe el selector (ej. en alguna página donde no se puso), salimos
    if(!selector) return;

    // 1. Cargar idioma guardado o defecto
    const currentLang = localStorage.getItem('selectedLang') || 'es';
    selector.value = currentLang;
    updateLanguage(currentLang);

    // 2. Escuchar cambios en el select
    selector.addEventListener('change', (e) => {
        const lang = e.target.value;
        localStorage.setItem('selectedLang', lang); // Guardar preferencia
        updateLanguage(lang);
    });
});

function updateLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                 element.placeholder = translations[lang][key];
            } else {
                 element.textContent = translations[lang][key];
            }
        }
    });
}